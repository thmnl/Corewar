#include "asm.h"

char			*ft_remove_com_from_cmd(char *s, char c)
{
	int i;
	int q;

	q = 0;
	i = 0;
	while (s[i])
	{
		if (s[i] == '\"')
			q++;
		if (s[i] == c && q == 2)
		{
			s[i] = '\0';
			return (s);
		}
		i++;
	}
	return (s);
}
