#include "libft.h"

int	ft_isescape(char *str, int pos)
{
	int i;

	if (pos < 1 || str[pos - 1] != '\\')
		return (0);
	i = pos - 1;
	while (i >= 0 && str[i] == '\\')
		i--;
	return (!((pos - i) % 2));
}
