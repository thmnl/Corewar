#include "libft.h"

char	*ft_swap_char(char *s, char c, char k)
{
	int i;

	i = 0;
	if (!s)
		return (s);
	while (s[i])
	{
		if (s[i] == c)
			s[i] = k;
		i++;
	}
	return (s);
}
